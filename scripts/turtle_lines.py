import turtle as t
t.setup(width=600, height=600)
t.speed(500)
t.color("lime")
t.bgcolor('black')
t.pu()
for i in range(-300, 300, 10):
    t.setpos(i,300)
    t.pd()
    t.setpos(300,i*-1)
    t.pu()
    t.setpos(i,-300)
    t.pd()
    t.setpos(300,i)
    t.pu()
for i in range(300, -300, -10):
    t.setpos(i,-300)
    t.pd()
    t.setpos(-300,i*-1)
    t.pu()
    t.setpos(i,300)
    t.pd()
    t.setpos(-300,i)
    t.pu()
for i in range(0, 300, 10):
    t.setpos(0,i)
    t.pd()
    t.setpos(i*-1,300)
    t.pu()
    t.setpos(i,0)
    t.pd()
    t.setpos(300,i*-1)
    t.pu()
for i in range(0, -300, -10):
    t.setpos(i,0)
    t.pd()
    t.setpos(-300,i*-1)
    t.pu()
    t.setpos(0,i)
    t.pd()
    t.setpos(i*-1,-300)
    t.pu()
for i in range(0, 300, 10):
    t.setpos(0,i)
    t.pd()
    t.setpos(i,300)
    t.pu()
    t.setpos(i,0)
    t.pd()
    t.setpos(300,i)
    t.pu()
for i in range(0, -300, -10):
    t.setpos(i,0)
    t.pd()
    t.setpos(-300,i)
    t.pu()
    t.setpos(0,i)
    t.pd()
    t.setpos(i,-300)
    t.pu()
t.exitonclick()
